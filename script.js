function normalizeUberPrice(distance, fare) {
    if(Math.random() < .25) {
        return 1;
    }
    var floatDistance = parseFloat(distance);
    var floatFare = parseFloat(fare);
    console.log("Normalized Quantity");
    console.log(floatFare/floatDistance);
    return (floatFare/floatDistance);
}

function calculateMins(time) {
    var splitTime = time.split(" ");
    var splitTime2 = splitTime[1].split(":");
    console.log(parseInt(splitTime2[0] * 60) + (parseInt(splitTime2[1])));
    return parseInt(splitTime2[0] * 60) + (parseInt(splitTime2[1]));
}

